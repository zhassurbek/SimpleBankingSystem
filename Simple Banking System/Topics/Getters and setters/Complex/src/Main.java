
class Complex {

    private double real;
    private double imaginary;

    public double getReal() {
        return real;
    }

    public double setReal(double real) {
        return this.real = real;
    }

    public double getImaginary() {
        return imaginary;
    }

    public double setImaginary(double imaginary) {
        return this.imaginary = imaginary;
    }
}